@register_tool(
    name="generate_xlsx",
    description="Generate an Excel spreadsheet (.xlsx) with styled sheets, charts, and optional summary rows. Returns {filename, filepath, format, title, template, file_size}.",
    parameters={
        "properties": {
            "title": {"type": "string", "description": "spreadsheet title"},
            "template": {"type": "string", "description": "one of: budget_tracker, comparison_sheet"},
            "sheets": {"type": "array", "description": "list of objects with keys: name (string), headers ([string]), rows ([[values]]), chart_type (optional: 'bar'|'pie'|'line'), summary (optional: 'totals'|'averages'|'both' — adds a summary row at the bottom for numeric columns)"},
        }
    },
)
async def generate_xlsx(
    title: str, template: str, sheets: list[dict]
) -> dict:
    from openpyxl import Workbook
    from openpyxl.styles import Font, PatternFill, Alignment, Border, Side
    from openpyxl.utils import get_column_letter
    from openpyxl.chart import BarChart, PieChart, LineChart, Reference

    wb = Workbook()
    wb.remove(wb.active)  # Remove default sheet

    # ── Styles ──
    header_font = Font(name="Calibri", size=12, bold=True, color="FFFFFF")
    header_fill = PatternFill(start_color=PRIMARY_COLOR, end_color=PRIMARY_COLOR, fill_type="solid")
    header_align = Alignment(horizontal="center", vertical="center")

    data_font = Font(name="Calibri", size=11)
    data_align = Alignment(horizontal="left", vertical="center")
    number_align = Alignment(horizontal="right", vertical="center")

    alt_fill = PatternFill(start_color=LIGHT_GRAY, end_color=LIGHT_GRAY, fill_type="solid")
    accent_fill = PatternFill(start_color="E8F5E9", end_color="E8F5E9", fill_type="solid")

    thin_border = Border(
        left=Side(style="thin", color="DDDDDD"),
        right=Side(style="thin", color="DDDDDD"),
        top=Side(style="thin", color="DDDDDD"),
        bottom=Side(style="thin", color="DDDDDD"),
    )

    for sheet_def in sheets:
        sheet_name = sheet_def.get("name", "Sheet")[:31]
        ws = wb.create_sheet(title=sheet_name)

        headers = sheet_def.get("headers", [])
        rows = sheet_def.get("rows", [])
        chart_type = sheet_def.get("chart_type")

        # ── Title row ──
        ws.merge_cells(start_row=1, start_column=1, end_row=1, end_column=max(len(headers), 1))
        title_cell = ws.cell(row=1, column=1, value=title)
        title_cell.font = Font(name="Calibri Light", size=18, bold=True, color=PRIMARY_COLOR)
        title_cell.alignment = Alignment(horizontal="center", vertical="center")
        ws.row_dimensions[1].height = 30

        # ── Headers ──
        if headers:
            for col_idx, header in enumerate(headers, 1):
                cell = ws.cell(row=3, column=col_idx, value=header)
                cell.font = header_font
                cell.fill = header_fill
                cell.alignment = header_align
                cell.border = thin_border
            ws.row_dimensions[3].height = 22

        # ── Data rows ──
        for row_idx, row in enumerate(rows, 4):
            for col_idx, val in enumerate(row, 1):
                cell = ws.cell(row=row_idx, column=col_idx, value=val)
                cell.font = data_font
                cell.border = thin_border

                # Alternate row coloring
                if (row_idx - 4) % 2 == 1:
                    cell.fill = alt_fill

                # Number alignment for numeric values
                if isinstance(val, (int, float)):
                    cell.alignment = number_align
                    # Currency format for budget sheets
                    if template == "budget_tracker" and col_idx > 1:
                        cell.number_format = '$#,##0.00'
                else:
                    cell.alignment = data_align

        # ── Auto column width ──
        for col_idx in range(1, len(headers) + 1):
            max_len = 0
            for row in ws.iter_rows(min_row=3, max_row=ws.max_row, min_col=col_idx, max_col=col_idx):
                for cell in row:
                    if cell.value:
                        max_len = max(max_len, len(str(cell.value)))
            ws.column_dimensions[get_column_letter(col_idx)].width = min(max_len + 4, 50)

        # ── Summary rows ──
        summary = sheet_def.get("summary")
        if summary and rows and headers:
            summary_row_start = len(rows) + 4
            summary_font = Font(name="Calibri", size=11, bold=True, color="FFFFFF")
            summary_fill = PatternFill(start_color="2d2d44", end_color="2d2d44", fill_type="solid")

            if summary in ("totals", "both"):
                ws.cell(row=summary_row_start, column=1, value="TOTAL").font = summary_font
                ws.cell(row=summary_row_start, column=1).fill = summary_fill
                ws.cell(row=summary_row_start, column=1).border = thin_border
                for col_idx in range(2, len(headers) + 1):
                    col_letter = get_column_letter(col_idx)
                    formula = f"=SUM({col_letter}4:{col_letter}{summary_row_start - 1})"
                    cell = ws.cell(row=summary_row_start, column=col_idx, value=formula)
                    cell.font = summary_font
                    cell.fill = summary_fill
                    cell.border = thin_border
                    cell.alignment = number_align
                    if template == "budget_tracker":
                        cell.number_format = '$#,##0.00'
                summary_row_start += 1

            if summary in ("averages", "both"):
                ws.cell(row=summary_row_start, column=1, value="AVERAGE").font = summary_font
                ws.cell(row=summary_row_start, column=1).fill = summary_fill
                ws.cell(row=summary_row_start, column=1).border = thin_border
                for col_idx in range(2, len(headers) + 1):
                    col_letter = get_column_letter(col_idx)
                    formula = f"=AVERAGE({col_letter}4:{col_letter}{summary_row_start - 1})"
                    cell = ws.cell(row=summary_row_start, column=col_idx, value=formula)
                    cell.font = summary_font
                    cell.fill = summary_fill
                    cell.border = thin_border
                    cell.alignment = number_align

        # ── Freeze panes ──
        if headers:
            ws.freeze_panes = "A4"

        # ── Chart ──
        if chart_type and len(rows) > 0 and len(headers) >= 2:
            chart_classes = {"bar": BarChart, "pie": PieChart, "line": LineChart}
            ChartClass = chart_classes.get(chart_type, BarChart)
            chart = ChartClass()
            chart.title = sheet_name
            chart.style = 10

            data = Reference(
                ws,
                min_col=2,
                min_row=3,
                max_col=len(headers),
                max_row=len(rows) + 3,
            )
            cats = Reference(ws, min_col=1, min_row=4, max_row=len(rows) + 3)
            chart.add_data(data, titles_from_data=True)
            chart.set_categories(cats)
            chart.width = 18
            chart.height = 10
            ws.add_chart(chart, f"A{len(rows) + 6}")

    filename = f"{uuid.uuid4().hex[:8]}_{title.replace(' ', '_')[:30]}.xlsx"
    filepath = os.path.join(GENERATED_FILES_DIR, filename)
    wb.save(filepath)

    return {
        "filename": filename,
        "filepath": filepath,
        "format": "xlsx",
        "title": title,
        "template": template,
        "file_size": os.path.getsize(filepath),
    }


