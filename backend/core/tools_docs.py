"""Document generation tools for the Worker Agent — professional formatting."""
import os
import uuid
from datetime import datetime

from core.tools import register_tool

GENERATED_FILES_DIR = os.path.join(
    os.path.dirname(os.path.dirname(__file__)), "generated_files"
)
os.makedirs(GENERATED_FILES_DIR, exist_ok=True)

# ─── Colors ───
HEX_TO_RGB = lambda h: tuple(int(h[i : i + 2], 16) for i in (0, 2, 4))

PRIMARY_COLOR = "1a1a2e"  # Dark navy
ACCENT_COLOR = "00ff88"  # Neon green
TEXT_COLOR = "333333"
LIGHT_GRAY = "f5f5f5"


def _snake_to_title(text: str) -> str:
    return " ".join(w.capitalize() for w in text.replace("_", " ").split())


# ═══════════════════════════════════════════════════════════════════════════════
# WORD DOCUMENT (.docx)
# ═══════════════════════════════════════════════════════════════════════════════

@register_tool(
    name="generate_docx",
    description="Generate a Word document (.docx) with text, tables, and embedded chart images. Returns {filename, filepath, format, title, template, file_size}.",
    parameters={
        "properties": {
            "title": {"type": "string", "description": "document title"},
            "template": {"type": "string", "description": "one of: travel_guide, project_plan, modern_report, research_report"},
            "sections": {"type": "array", "description": "list of objects with keys: heading (string), content (string), table (optional: {headers: [string], rows: [[values]]}), chart_image (optional: filepath from generate_chart)"},
        }
    },
)
async def generate_docx(title: str, template: str, sections: list[dict]) -> dict:
    from docx import Document
    from docx.shared import Inches, Pt, RGBColor
    from docx.enum.text import WD_ALIGN_PARAGRAPH
    from docx.enum.table import WD_TABLE_ALIGNMENT
    from docx.enum.section import WD_ORIENT

    doc = Document()

    # ── Styles ──
    style = doc.styles["Normal"]
    font = style.font
    font.name = "Calibri"
    font.size = Pt(11)
    font.color.rgb = RGBColor(*HEX_TO_RGB(TEXT_COLOR))

    # Heading 1 style
    h1 = doc.styles["Heading 1"]
    h1.font.name = "Calibri Light"
    h1.font.size = Pt(20)
    h1.font.bold = True
    h1.font.color.rgb = RGBColor(*HEX_TO_RGB(PRIMARY_COLOR))

    # Heading 2 style
    h2 = doc.styles["Heading 2"]
    h2.font.name = "Calibri Light"
    h2.font.size = Pt(16)
    h2.font.bold = True
    h2.font.color.rgb = RGBColor(*HEX_TO_RGB(ACCENT_COLOR))

    # ── Cover Page ──
    doc.add_paragraph()  # spacing
    doc.add_paragraph()
    title_para = doc.add_paragraph()
    title_run = title_para.add_run(title)
    title_run.font.size = Pt(32)
    title_run.font.bold = True
    title_run.font.color.rgb = RGBColor(*HEX_TO_RGB(PRIMARY_COLOR))
    title_run.font.name = "Calibri Light"
    title_para.alignment = WD_ALIGN_PARAGRAPH.CENTER

    subtitle = doc.add_paragraph()
    sub_run = subtitle.add_run(_snake_to_title(template))
    sub_run.font.size = Pt(16)
    sub_run.font.color.rgb = RGBColor(*HEX_TO_RGB(ACCENT_COLOR))
    sub_run.font.name = "Calibri Light"
    subtitle.alignment = WD_ALIGN_PARAGRAPH.CENTER

    doc.add_paragraph()
    date_para = doc.add_paragraph()
    date_run = date_para.add_run(
        f"Generated by Life OS — {datetime.now().strftime('%B %d, %Y')}"
    )
    date_run.font.size = Pt(10)
    date_run.font.color.rgb = RGBColor(128, 128, 128)
    date_run.font.italic = True
    date_para.alignment = WD_ALIGN_PARAGRAPH.CENTER

    doc.add_page_break()

    # ── Sections ──
    for section in sections:
        heading = section.get("heading", "")
        content = section.get("content", "")

        doc.add_heading(heading, level=1)

        # Content paragraphs
        for para_text in content.split("\n"):
            para_text = para_text.strip()
            if not para_text:
                continue
            if para_text.startswith("- ") or para_text.startswith("• "):
                p = doc.add_paragraph(para_text[2:].strip(), style="List Bullet")
            elif para_text.startswith("1. ") or para_text.startswith("2. "):
                p = doc.add_paragraph(para_text[3:].strip(), style="List Number")
            else:
                p = doc.add_paragraph(para_text)

        # Optional table
        table_data = section.get("table")
        if table_data:
            headers = table_data.get("headers", [])
            rows = table_data.get("rows", [])
            if headers and rows:
                table = doc.add_table(rows=1 + len(rows), cols=len(headers))
                table.style = "Table Grid"
                table.alignment = WD_TABLE_ALIGNMENT.CENTER

                # Header row
                for i, h in enumerate(headers):
                    cell = table.rows[0].cells[i]
                    cell.text = str(h)
                    for paragraph in cell.paragraphs:
                        for run in paragraph.runs:
                            run.font.bold = True
                            run.font.color.rgb = RGBColor(255, 255, 255)
                    cell.paragraphs[0].alignment = WD_ALIGN_PARAGRAPH.CENTER
                    # Dark background
                    shading = cell._element.get_or_add_tcPr()
                    from docx.oxml.ns import qn

                    shading.append(
                        parse_xml(
                            r'<w:shd {} w:fill="{}"/>'.format(
                                nsdecls("w"), PRIMARY_COLOR
                            )
                        )
                    )

                # Data rows
                for r_idx, row in enumerate(rows):
                    for c_idx, val in enumerate(row):
                        cell = table.rows[r_idx + 1].cells[c_idx]
                        cell.text = str(val)
                        cell.paragraphs[0].alignment = WD_ALIGN_PARAGRAPH.CENTER
                        # Alternate row colors
                        if r_idx % 2 == 1:
                            shading = cell._element.get_or_add_tcPr()
                            shading.append(
                                parse_xml(
                                    r'<w:shd {} w:fill="{}"/>'.format(
                                        nsdecls("w"), LIGHT_GRAY
                                    )
                                )
                            )

        # Optional chart image
        chart_image = section.get("chart_image")
        if chart_image and os.path.exists(chart_image):
            doc.add_paragraph()
            doc.add_picture(chart_image, width=Inches(5.5))
            last_paragraph = doc.paragraphs[-1]
            last_paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER

        doc.add_paragraph()  # spacing between sections

    # ── Footer ──
    for section in doc.sections:
        footer = section.footer
        footer_para = footer.paragraphs[0] if footer.paragraphs else footer.add_paragraph()
        footer_para.text = f"Life OS — {title} — Page "
        footer_para.alignment = WD_ALIGN_PARAGRAPH.CENTER

    filename = f"{uuid.uuid4().hex[:8]}_{title.replace(' ', '_')[:30]}.docx"
    filepath = os.path.join(GENERATED_FILES_DIR, filename)
    doc.save(filepath)

    return {
        "filename": filename,
        "filepath": filepath,
        "format": "docx",
        "title": title,
        "template": template,
        "file_size": os.path.getsize(filepath),
    }


