# PDF DOCUMENT
# ═══════════════════════════════════════════════════════════════════════════════

@register_tool(
    name="generate_pdf",
    description="Generate a PDF document with text, tables, and embedded chart images. Returns {filename, filepath, format, title, template, file_size}.",
    parameters={
        "properties": {
            "title": {"type": "string", "description": "document title"},
            "template": {"type": "string", "description": "one of: research_report, modern_report"},
            "sections": {"type": "array", "description": "list of objects with keys: heading (string), content (string), table (optional: {headers: [string], rows: [[values]]}), chart_image (optional: filepath from generate_chart)"},
        }
    },
)
async def generate_pdf(title: str, template: str, sections: list[dict]) -> dict:
    from reportlab.lib.pagesizes import letter
    from reportlab.lib.units import inch
    from reportlab.lib.colors import HexColor, white, black
    from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
    from reportlab.platypus import (
        SimpleDocTemplate,
        Paragraph,
        Spacer,
        Table,
        TableStyle,
        PageBreak,
    )
    from reportlab.pdfgen import canvas

    filename = f"{uuid.uuid4().hex[:8]}_{title.replace(' ', '_')[:30]}.pdf"
    filepath = os.path.join(GENERATED_FILES_DIR, filename)

    doc = SimpleDocTemplate(
        filepath,
        pagesize=letter,
        rightMargin=72,
        leftMargin=72,
        topMargin=72,
        bottomMargin=72,
    )

    # ── Custom Styles ──
    styles = getSampleStyleSheet()

    title_style = ParagraphStyle(
        "CustomTitle",
        parent=styles["Title"],
        fontSize=28,
        textColor=HexColor(f"#{PRIMARY_COLOR}"),
        spaceAfter=12,
        fontName="Helvetica-Bold",
    )

    subtitle_style = ParagraphStyle(
        "CustomSubtitle",
        parent=styles["Normal"],
        fontSize=14,
        textColor=HexColor(f"#{ACCENT_COLOR}"),
        spaceAfter=30,
        fontName="Helvetica-Oblique",
    )

    heading1_style = ParagraphStyle(
        "CustomH1",
        parent=styles["Heading1"],
        fontSize=18,
        textColor=HexColor(f"#{PRIMARY_COLOR}"),
        spaceAfter=12,
        spaceBefore=18,
        fontName="Helvetica-Bold",
    )

    heading2_style = ParagraphStyle(
        "CustomH2",
        parent=styles["Heading2"],
        fontSize=14,
        textColor=HexColor(f"#{ACCENT_COLOR}"),
        spaceAfter=8,
        spaceBefore=12,
        fontName="Helvetica-Bold",
    )

    body_style = ParagraphStyle(
        "CustomBody",
        parent=styles["Normal"],
        fontSize=11,
        leading=16,
        textColor=black,
        fontName="Helvetica",
    )

    story = []

    # ── Cover Page ──
    story.append(Spacer(1, 2 * inch))
    story.append(Paragraph(title, title_style))
    story.append(Paragraph(_snake_to_title(template), subtitle_style))
    story.append(Spacer(1, 0.5 * inch))
    story.append(
        Paragraph(
            f"Generated by Life OS — {datetime.now().strftime('%B %d, %Y')}",
            ParagraphStyle(
                "Date",
                parent=styles["Normal"],
                fontSize=10,
                textColor=HexColor("#888888"),
                alignment=1,
                fontName="Helvetica-Oblique",
            ),
        )
    )
    story.append(PageBreak())

    # ── Sections ──
    for section in sections:
        heading = section.get("heading", "")
        content = section.get("content", "")

        story.append(Paragraph(heading, heading1_style))

        for para_text in content.split("\n"):
            para_text = para_text.strip()
            if not para_text:
                continue
            if para_text.startswith("### "):
                story.append(Paragraph(para_text[4:], heading2_style))
            elif para_text.startswith("- ") or para_text.startswith("• "):
                story.append(Paragraph(f"• {para_text[2:]}", body_style))
            else:
                story.append(Paragraph(para_text, body_style))

        # Optional table
        table_data = section.get("table")
        if table_data:
            headers = table_data.get("headers", [])
            rows = table_data.get("rows", [])
            if headers and rows:
                data = [headers] + [[str(c) for c in row] for row in rows]
                t = Table(data, hAlign="LEFT")
                t.setStyle(
                    TableStyle(
                        [
                            ("BACKGROUND", (0, 0), (-1, 0), HexColor(f"#{PRIMARY_COLOR}")),
                            ("TEXTCOLOR", (0, 0), (-1, 0), white),
                            ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
                            ("FONTSIZE", (0, 0), (-1, 0), 11),
                            ("BOTTOMPADDING", (0, 0), (-1, 0), 10),
                            ("BACKGROUND", (0, 1), (-1, -1), HexColor("#f9f9f9")),
                            ("GRID", (0, 0), (-1, -1), 0.5, HexColor("#dddddd")),
                            ("FONTNAME", (0, 1), (-1, -1), "Helvetica"),
                            ("FONTSIZE", (0, 1), (-1, -1), 10),
                            ("ROWBACKGROUNDS", (0, 1), (-1, -1), [white, HexColor("#f5f5f5")]),
                            ("ALIGN", (0, 0), (-1, -1), "LEFT"),
                            ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
                        ]
                    )
                )
                story.append(Spacer(1, 12))
                story.append(t)

        # Optional chart image
        chart_image = section.get("chart_image")
        if chart_image and os.path.exists(chart_image):
            from reportlab.platypus import Image as RLImage
            story.append(Spacer(1, 12))
            img = RLImage(chart_image, width=5.5 * inch, height=3.3 * inch)
            story.append(img)

        story.append(Spacer(1, 18))

    # ── Footer with page numbers ──
    def add_page_num(canvas, doc):
        canvas.saveState()
        canvas.setFont("Helvetica", 9)
        canvas.setFillColor(HexColor("#888888"))
        canvas.drawCentredString(
            letter[0] / 2, 0.5 * inch, f"Life OS — {title} — Page {doc.page}"
        )
        canvas.restoreState()

    doc.build(story, onFirstPage=add_page_num, onLaterPages=add_page_num)

    return {
        "filename": filename,
        "filepath": filepath,
        "format": "pdf",
        "title": title,
        "template": template,
        "file_size": os.path.getsize(filepath),
    }
